# This is a bash script that writes text to a file named 'output.txt'

# The '>>' operator is used to append text to the file
echo "Hello world" > output.txt

# The '>' operator is used to overwrite the file with new text
# In this case, the file 'output.txt' is overwritten twice with the same text
echo "This is a new line " >> output.txt
echo "This is laamiri " >> output.txt